float sin(int deg){
    deg %= 360;
    return sin_table[deg];
}
float cos(int deg){
    return sin(deg+90);
}
int myRound(float a){
    int result = 0;
    while(a >= 1) {
        result++;
        a--;
    }
    while(a <= -1){
        result--;
        a++;
    }
    if(a <= -0.5) result--;
    if(a >= 0.5) result++;
    return result;
}

